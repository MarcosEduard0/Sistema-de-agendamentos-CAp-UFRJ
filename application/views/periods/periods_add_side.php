  <h6>Formato de hora</h6>
  <dd>Os formatos suportados para os campos Horário de início e Horário de término são <strong>HHMM</strong> ou <strong>HH:MM</strong>. Por exemplo, <em>0930</em> ou <em>14:20</em>. Você também deve incluir o 0 à esquerda se estiver usando o formato HHMM</dd>
  <dd>Todos os horários também devem ser inseridos no formato de <strong>24 horas</strong> e estar entre 00:00 e 23:59.</dd>
