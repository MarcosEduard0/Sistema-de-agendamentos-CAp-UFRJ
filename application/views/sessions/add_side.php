<div class="bd-callout bd-callout-info">

	<dt>Sessão</dt>
	<dd>Cada sessão normalmente dura todo o ano letivo. Defina as datas de início e término da sessão aqui.</dd>

	<dt>Formato da data</dt>
	<dd>Use o formato <span>DD/MM/YYYY</span> ao inserir datas. Por esemplo <em>16/04/2018</em>.</dd>

	<dt>Alterando datas</dt>
	<dd>Se você alterar a data de início ou término após os agendamentos terem sido feitas durante a sessão, os agendamentos existentes <em>fora do novo intervalo de datas</em> serão excluídas.</dd>
